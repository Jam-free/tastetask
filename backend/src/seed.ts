async function main() {
  console.log('🌱 开始导入种子数据...')

  // 清空现有数据（开发环境）
  await prisma.favorite.deleteMany({})
  await prisma.case.deleteMany({})
  console.log('✅ 已清空现有数据')

  // 导入预置用例
  let count = 0
  for (const item of PRESET_CASES) {
    await prisma.case.create({
      data: item,
    })
    count++
  }

  console.log(`✅ 已导入 ${count} 条预置用例`)
  console.log('🎉 种子数据导入完成!')
}

main()
  .catch((e) => {
    console.error('❌ 种子数据导入失败:', e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
